using DreamCMS.Authorize;
using DreamCMS.Models;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web.Mvc;

namespace DreamCMS.Areas.Admin.Controllers
{
    public class OrdersController : Controller
    {
        private DBFrontEnd db = new DBFrontEnd();

        // GET: Admin/Orders
        [Auth]
        public ActionResult Index()
        {
            return View(db.Orders.OrderBy(x=>x.IsSeen).ThenByDescending(x=>x.OrderId).ToList());
        }

        // GET: Admin/Orders/Details/5
        [Auth]
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Order order = db.Orders.Find(id);
            if (order == null)
            {
                return HttpNotFound();
            }

            order.IsSeen = true;
            db.SaveChanges();

            ViewBag.sp = db.OrderDetails.Where(p => p.OrderId == id).ToList();
            return View(order);
        }

